package com.hfpp.network.models;

/**
 * <p>
 * The SortType enumeration represents the sorting type (Ascending, Descending).
 * </p>
 *
 * <p>
 * <strong>Thread Safety: </strong> This enumeration is immutable and thread safe.
 * </p>
 *
 * @author flying2hk, sparemax
 * @version 1.0
 */
public enum SortType {
    /**
     * Represents the descending sort type.
     */
    DESC,

    /**
     * Represents the ascending sort type.
     */
    ASC
}
